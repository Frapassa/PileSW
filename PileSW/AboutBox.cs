        string GetVer(AssemblyName l)
        {
            return l.Version.Major.ToString().PadLeft(2, '0') + "." + l.Version.Minor.ToString().PadLeft(2, '0') + "." + l.Version.Build.ToString().PadLeft(2, '0') + "." + l.Version.Revision.ToString().PadLeft(4, '0');
    
        }
